#~ Viajeros en las Tierras Reclamadas

#~ 01-07 Constructores Enanos: 1d8 enanos expertos (nivel 1d4 cada uno) se dirigen a o
#~ desde Nueva Alasia para trabajos de construcción.
#~ 
#~ 08-19 Mercaderes/Caravana: Uno o más comerciantes transportando
#~ mercancías. Tirar en la tabla de Caravanas y Mercaderes.
#~ 
#~ 20-31 Partida de Caza: Un grupo de 1d8 personas cazando para comer.
#~ Son commoners de nivel 1 liderados por un ranger de nivel 2.
#~ 
#~ 32-38 Partida de Caza: Un grupo de 1d10 aristocratas de nivel 1d3 cazando por
#~ diversión, guiados por un maestro de caza (ranger de 2) y 1d3 criados por
#~ aristocrata. Tienen un 75% de ir a caballo, y un 60% de tener perros o animales de
#~ caza similares.
#~ 
#~ 39-42 Exploradores Sarathan: Un grupo de jinetes sarathan patrullando las Tierras
#~ Reclamadas. Intentarán observar a los PJs pero evitando una confrontación. El
#~ grupo consiste en 1d6 miembros, cada uno un ranger o guerrero de nivel 1d4.
#~ 
#~ 43-46 Mercenarios: Una pequeña fuerza militar en busca de trabajo, o en su defecto, de
#~ saqueo y pillaje. Consiste en 3d20 miembros comunes (soldados de 1), liderados por
#~ un guerrero, ranger o bárbaro de nivel 1 por cada 5 miembros, un guerrero, ranger
#~ o bárbaro de nivel 3 por cada 10 miembros y un líder guerrero, ranger o bárbaro de
#~ nivel 5-7. El grupo tiene un 1% por miembro de tener un clérigo o adepto de nivel
#~ 3, y un 1% por miembro de tener a un mago o hechicero de 3.
#~ 
#~ 47-58 Patrulla a Pie: Soldados y milicianos de Alasia manteniendo los caminos seguros.
#~ 3d6 soldados comunes, dos tenientes de nivel 2 y un líder de nivel 4.
#~ 
#~ 59-65 Patrulla Montada: Soldados Alasianos montados en caballos de guerra ligeros. Hay
#~ 2d8 soldados comunes, un teniente (caballero de nivel 2) y un líder (caballero de 4).
#~ 
#~ 66-72 Bandidos: Una banda semiorganizada de 2d10 rogues y soldados de nivel 1,
#~ liderados por un único líder (tirar en la tabla de Bandidos)
#~ 
#~ 73-76 Peregrinos: Un grupo de 2d10 adoradores de una deidad concreta (1d10: 1 -2
#~ Uriel, 3-4 Athor, 5-6 Aysla, 7 Gardron, 8 Arkath, 9 Eresh, 10 Awen) guiados por
#~ un líder clérigo de nivel 4. Los peregrinos son en un 80% commoners, pero el resto
#~ son clérigos o monjes de nivel 1d3.
#~ 
#~ 77-80 Caballero Solitario: Un Caballero Protector (80%) o un Guardia Negro (20%); es un
#~ guerrero, paladín o caballero de nivel 1d4+2. Siempre va montado en un caballo de
#~ guerra pesado y está bien armado. Tiene un 80% de tener 1d3 sirvientes o esclavos.
#~ 81-84 Esclavistas Kanthianos: 4d6 esclavos encadenados custodiados por 2d4 soldados de
#~ nivel 1 y 1d3 tenientes de nivel 3. El grupo está liderado por un experto de nivel
#~ 3-6.
#~ 
#~ 85-96 Lugareños: Un grupo de 1d10 lugareños granjeros, leñadores, etc viajando por el
#~ camino, todos commoners de nivel 1-2.
#~ 
#~ 97-100 Aventureros: Un grupo de 1d8 aventureros rivales; tirar la composición en la tabla
#~ de Aventureros. Cada uno es de nivel 1d4+1. 
